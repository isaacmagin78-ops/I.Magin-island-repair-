function getDefaultMoveInDate(): string {
  const today = new Date();
  const nextAugust = new Date(today.getFullYear(), 7, 15);
  if (today > nextAugust) {
    nextAugust.setFullYear(nextAugust.getFullYear() + 1);
  }
  return nextAugust.toISOString().split('T')[0];
}
